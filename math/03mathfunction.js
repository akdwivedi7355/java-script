// abstract value of a number 
console.log(Math.abs(-4));
console.log(Math.abs(4));


// round off of a number 
console.log(Math.round(4.55));
console.log(Math.round(6.39));
console.log(Math.round(67.876));

// ceil->for upper value 
console.log(Math.ceil(4.55));
console.log(Math.ceil(6.35));

// floor-> for lower value
console.log(Math.floor(4.55));
console.log(Math.floor(6.35));

